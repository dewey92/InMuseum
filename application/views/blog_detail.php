					<p class="blog-detail-info">by <a href="">Puspita Awalina</a> | 12 Dec 2014</p>
					<div class="blog-detail-content">
						<p>Hampir 2 tahun saya tinggal di Ibukota Jakarta, tapi tak sekalipun saya pernah menginjakkan kaki di museum nasional, padahal hampir setiap hari saya melewati gedung yang terletak tepat di depan halte busway Monas ini.  Dengan seorang teman, akhirnya saya memutuskan untuk mengunjungi museum nasional atau lebih dikenal dengan sebutan museum gajah.</p>
						<p>Tepat pada pukul 10.30 WIB, saya menginjakkan kaki di depan museum nasional dan langsung menuju loket pembelian tiket masuk. Dengan harga tiket Rp 750 (terus terang saya cukup kaget dengan harga tiket masuk semurah itu), saya masuk dan mulai menikmati koleksi-koleksi yang terdapat di museum nasional.</p>
						<p>Museum nasional saat ini memiliki dua buah gedung, yaitu gedung lama dan gedung baru. Dari gedung lama ke gedung baru dihubungkan dengan sebuah bangunan dengan dinding kaca dikedua sisinya dan pengunjung dapat menikmati taman yang berada di bawahnya (sebetulnya bukan taman, tapi lebih terlihat seperti Plaza dengan air mancur yang terletak di ujung, namun sayang pada saat itu air mancur tidak menyala).</p>
						<p>Ruangan pertama yang saya kunjungi adalah ruangan Arca, dimana pada ruangan ini saya dapat melihat arca peninggalan kerajaan – kerajaan Indonesia pada masa lampau.  Setelah kurang lebih 15 menit berputar menikmati keindahan arca, saya masuk ke ruang keramik. Banyak sekali koleksi keramik yang dipajang pada ruangan ini, dari mulai keramik sisa-sisa peninggalan kerajaan Indonesia sampai dengan keramik yang berasal dari luar (sebagian besar dari China). Pada ruangan ini juga saya bisa melihat gambar yang menunjukkan proses pembuatan keramik.</p>
						<p>Ruangan selanjutnya yang saya kunjungi menampilkan miniatur rumah-rumah adat dari masing-masing daerah di Indonesia. Miniatur yang terbuat dari kayu tersebut hampir menyerupai rumah ada yang sesungguhnya. Namun sayang, menurut saya penataan koleksi pada ruangan ini kurang baik dan terkesan seperti gudang. Telebih lagi udara yang panas (karena tidak ada AC) membuat saya ingin segera keluar dan pindah ke ruangan lain.</p>
						<p>Tak berapa lama, saya masuk ke ruang selanjutnya. Ruangan ini jauh lebih nyaman karena memiliki pendingin udara di dalamnya. Pada awalnya saya berpikir ini adalah gedung baru dari museum nasional, namun ternyata ini adalah sayap kiri dari gedung lama. Ruangan ini menampilkan koleksi benda-benda yang berasal dari propinsi yang ada di Indonesia, dari mulai Aceh sampai dengan Papua.</p>
						<p>Setelah  1,5 jam berputar di gedung lama, saya mulai beranjak ke gedung yang baru. Gedung ini jauh lebih modern dibandingkan dengan gedung lama dan memiliki 4 lantai dimana masing-masing lantai memiliki tema yang berbeda. Lantai pertama menampilkan koleksi  benda-benda prasejarah yang ditemukan di Indonesia, mulai dari fosil tengkorak phitecanthropus erectus sampai dengan fosil manusia kerdil dari flores. Lantai kedua dan ketiga saya lupa menampilkan koleksi apa, namun ternyata beberapa benda yang disimpan pada gedung lama juga ada yang disimpan di sini, sehingga sebagian besar benda yang ditampilkan pada kedua lantai ini sama dengan benda yang ditampilkan pada gedung lama.</p>
						<p>Terakhir adalah lantai 4, lantai yang paling saya sukai. Di lantai 4, terdapat ruangan yang bernama khazanah emas dan perak. Pada ruangan ini terdapat koleksi benda-benda yang terbuat dari emas dan perak yang berasal dari kerajaan – kerajaan di Indonesia pada masa lampau. Cukup lama saya berada di lantai 4, menikmati keindahan benda-benda tersebut. Andaikan kerajaan-kerajaan di Indonesia tidak runtuh, apa yang akan terjadi dengan Indonesia ya?</p>
						<p>Mengunjungi Museum Nasional membuat saya semakin sadar bahwa Indonesia adalah negara yang kaya akan budaya. Andaikata pemerintah lebih memperhatikan potensi pariwisata Indonesia, saya yakin Indonesia akan menjadi salah satu negara tujuan wisata yang paling banyak peminatnya di dunia.</p>
						<p>Tak terasa waktu sudah menunjukkan pukul 13.30, kini saat nya menuju ke tujuan selanjutnya yaitu Museum Bank Indonesia yang terletak di daerah Kota.</p>
